// Crie uma variável para cada tipo primitivo em JavaScript: string, number, boolean, null e undefined.
// Atribua um valor a cada variável de acordo com o tipo correspondente.
// Em seguida, utilize o console.log para exibir o valor e o tipo de cada variável.

let String = "Leticia faz ECO";
console.log("Valor:", String);
console.log("Tipo:", typeof String);


let Numero = 29;
console.log("Valor:", Numero);
console.log("Tipo:", typeof Numero);


let Bool = false;
console.log("Valor:", Bool);
console.log("Tipo:", typeof Bool);


let Null = null;
console.log("Valor:", Null);
console.log("Tipo:", typeof Null);


let Undefined;
console.log("Valor:", Undefined);
console.log("Tipo:", typeof Undefined);
